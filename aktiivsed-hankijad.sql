SELECT TOI_TOIMITTAJANRO, TOI_NIMI
FROM TOIMPER
LEFT JOIN TUOTVAIH ON TOI_TOIMITTAJANRO = TV_TOIMITTAJANRO
WHERE TV_VARASTOSSA > 0 AND TV_TOIMITTAJANRO NOT IN (400, 678, 679, 680, 682, 684, 689, 691, 692, 697, 699, 704, 705, 707, 717, 725, 728, 6000, 8506, 8507, 495, 1145, 498)
GROUP BY TOI_TOIMITTAJANRO, TOI_NIMI